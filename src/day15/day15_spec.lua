local day15 = require("day15")

describe("day15", function()
    describe("small example", function()
        local input = [[########
#..O.O.#
##@.O..#
#...O..#
#.#.O..#
#...O..#
#......#
########

<^^>>>vv<v>>v<<
]]
        local part1, part2 = day15.solve(input)
        it("part1", function()
            assert.are.equal(2028, part1)
        end)
        it("part2", function()
            assert.are.equal(0, part2)
        end)
    end)

